//find the larger number 
function largerNumber() {
var num1 = parseInt(prompt("Enter the First Number"));
var num2 = parseInt(prompt("Enter the Second Number"));
var output = ""
if (num1 > num2) {
    output = num1;
}
else if (num2 > num1) {
    output = num2;
}
else if (num1==num2) {
    output = ("Both are Equal")
}
else {
    output = ("Invalid Answer")
}
}

